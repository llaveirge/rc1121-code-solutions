// Define the function 'setValue' with three parameters: 'object', 'key', and 'value'
// Assign the 'value' parameter as the value for the 'key' parameter in the 'object' parameter
// Use bracket notation to access a property in an object in order to assign it a value
// Do not return anything from the function

function setValue(object, key, value) {
  object[key] = value;
}
